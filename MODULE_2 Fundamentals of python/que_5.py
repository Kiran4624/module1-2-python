"""What is the purpose continue statement in python?

Python Continue Statement skips the execution of the 
program block after the continue statement 
and forces the control to start the next iteration.

A continue statement ends the current iteration of a loop .
Program control is passed from the continue statement to the end of the loop body. 
A continue statement can only appear within the body of an iterative statement, 
such as do, for, or while.


Python continue Statement Syntax
while True:
    ...
    if x == 10:
        continue
    print(x)"""